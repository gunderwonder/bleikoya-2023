			<h2>Historie</h2>
			<p>Navnet «Bleikøya», tidligere Bleegøen, kommer av det norrøne «bleikr», som betyr lys.</p>
			<p>Tidligere var øya klostergods under cisterniensermunkene på Hovedøya. Etter reformasjonen ble øya gjort om til krongods. Da Karl 12. rykket fram mot byen i 1716, søkte soldatene ly bak Bleikøya for kanonaden fra Akershus festning. Ytterst på nordspissen ligger Bleikøya lykt fra 1906. På nordøstsiden ligger Bleikøykalven med fyrlykt på Kalvodden.</p>
			<p>Bleikøya rommer et gammelt gårdsbruk, det eneste på øyene. Det drives ikke lenger nå, men har i nesten 300 år tilhørt samme familie. Det ligger i østenden mot Bleikøykalven.</p>
			<p>På Bleikøya har det også vært annen virksomhet. Et stort sommerhus, det eneste på øya i nesten 50 år, ble bygget i 1864 av Jørgen Gjerdrum. Det ble i 1885 omgjort til sanatorium for turberkuløse barn, og har siden 1926 vært øyas store velhus og samlingssted med butikk, med egen trallebane ned til bryggen.</p>
			<p>Fra 1923 begynte så byggingen av småhytter som på Nakholhmen og Lindøya. Den siste hytta sto ferdig i 1938. Men som alle hytteeiere vet, blir en hytte aldri helt ferdig.</p>

