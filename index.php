			<h2>Besøkende</h2>
			<p>Bleikøya er kupert, og tilbyr stier og sjelden vegetasjon i kombinasjon med tettliggende hytter. Ifølge Byantikvaren har bebyggelsen stor kulturhistorisk verdi, og viser arbeiderkultur fra 1920-tallet. Hyttene ble i sin tid plassert av arkitekt Alnæs, og plasseringen ble gjort på en måte som både skulle passe inn i terrenget samtidig som hver hytte skulle ha tilstrekkelig utsyn.</p>
